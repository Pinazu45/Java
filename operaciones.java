public class operaciones
{
    public static void main(String[]args)
    {
        int a = 11, b = 5, s, r, m, d;
        s = a + b; //Se crean variables extras para guardar resultados
        r = a - b;
        m = a * b;
        d = a / b;
        System.out.print("El resultado de 11 + 5 es: " + s);
        System.out.print("\nEl resultado de 11 - 5 es: " + r);
        System.out.print("\nEl resultado de 11 * 5 es: " + m);
        System.out.print("\nEl resultado de 11 / 5 es: " + d);

        System.out.print("Esta serian las operaciones basicas");
    }
}
